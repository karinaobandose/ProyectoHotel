    @Override
    public void showData() {
        DefaultTableModel table = (DefaultTableModel) this.tbListaHab.getModel();
        table.setRowCount(0);
        if (this.controlador.getLista() != null) {
            Iterator<MCliente> iterator = this.controlador.getLista().iterator();
            while (iterator.hasNext()) {
                MCliente Cliente = iterator.next();

                Integer Cedula = Cliente.getCedula();
                String Nombre = Cliente.getNombre();
                String Fecha = String.valueOf(Cliente.getFechaNacimiento());
                String Telefono = Cliente.getTelefono();
                String Correo = Cliente.getCorreo();

                table.addRow(new Object[]{Cedula, Nombre, Fecha, Telefono, Correo});
            }
        }
        this.tbListaHab.setModel(table);
    }

    
    
