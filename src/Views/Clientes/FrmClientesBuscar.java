    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

        if (this.tbListaHab.getRowCount() > 0 && this.tbListaHab.getSelectedRow() > -1) {

            Integer Cedula = ((Integer) this.tbListaHab.getValueAt(this.tbListaHab.getSelectedRow(), 0));
            String Nombre = ((String) this.tbListaHab.getValueAt(this.tbListaHab.getSelectedRow(), 1));
            String Fecha = ((String) this.tbListaHab.getValueAt(this.tbListaHab.getSelectedRow(), 2));
            String Telefono = ((String) this.tbListaHab.getValueAt(this.tbListaHab.getSelectedRow(), 3));
            String Correo = ((String) this.tbListaHab.getValueAt(this.tbListaHab.getSelectedRow(), 4));

            MCliente Cliente = new MCliente(Cedula, Nombre, LocalDate.parse(Fecha),Telefono,Correo);
            this.controlador.setObjecto(Cliente);
            this.dispose();
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

